					      </div>
					    <div class="collapsible-body"><p>A ecologia química é o estudo das interações químicas usadas na comunicação entre
						organismos. Existem diferentes métodos para extração e identificação desses sinais químicos.
						Nessa palestra abordaremos como acontece o estudo químico dessas moléculas, desde os
						diferentes métodos de coleta de voláteis a diferentes métodos de análises para identificação.
						Diante disso, o objetivo dessa palestra é apresentar de forma simples e dinâmica os métodos
						para extração de semioquímicos, ou seja, os sinais químicos que intermediam a comunicação
						dos insetos e das plantas e também como é feito o processo para identificação desses
						compostos químicos. Além de expandir esse conhecimento para os estudantes de outras
						áreas de pesquisa, podendo assim gerar novas ideias de interação entre linhas de pesquisas
						diferentes. Por fim, espera-se agregar novas perspectivas do que vem sendo pesquisado nos
						últimos tempos na Ecologia Química que tem contribuído para a sociedade.
