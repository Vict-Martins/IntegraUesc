					    <div class="collapsible-body">
					    	<p>A multimistura é definido como sendo um tipo de farinha composta por vários alimentos secos,
							que tem sido aplicado com sucesso na alimentação de crianças desnutridas. Os principais
							componentes usados na mistura são: Farelo de Arroz, Flocos de milho, Farinha de folha de
							mandioca, Casca de ovo, semente de abóbora e melão, dentre outras, que, quando misturadas
							em proporções adequadas, fornecem quantidades excelentes de macronutrientes (proteínas,
							carboidratos e lipídios) e micro nutrientes como minerais (Cálcio, ferro, potássio, magnésio,
							etc). Cada região, possui um potencial próprio para produção de uma multimistura com matéria
							prima própria, como no alto sertão que possui potencial para produção de farinha de gergilin. A
							região de Ilheus possui uma série de alimentos e resíduos agroindustriais com potencial de
							fabricação de multimistura, com destaque para a farinha de caroço de jaça, com quantidades
							significativas de carboidratos, proteínas e minerais, e com potencial para cultivo de novas
							variedades como ora pro nobis que é rica em proteínas. A multimistura já foi usada com
							sucesso por entidades como a Pastoral da Criança, tirando milhões de crianças e idosos da
							desnutrição, o que torna seu uso possível em Ilhéus, devido a grande ocorrência de crianças
